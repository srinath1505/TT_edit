    <div class="copy-trading-grid">
      <article class="copy-trading-card copy-trading-card--positive">
        <div class="doc-page-hero-title">Advantages</div>
        <h2>Why some users choose copy trading</h2>
        <ul class="copy-trading-list">
          <li><strong>Beginner-Friendly:</strong> Allows newer users to participate without deep technical knowledge.</li>
          <li><strong>Time-Efficient:</strong> Trades are executed automatically, reducing the need for constant monitoring.</li>
          <li><strong>Learning Opportunity:</strong> Users can observe trading behavior and strategies over time.</li>
          <li><strong>Diversification:</strong> Funds can be allocated across multiple traders with different approaches.</li>
        </ul>
      </article>

      <article class="copy-trading-card copy-trading-card--warning">
        <div class="doc-page-hero-title">Risks</div>
        <h2>What users should understand clearly</h2>
        <ul class="copy-trading-list">
          <li><strong>Market Risk:</strong> Financial markets remain volatile, even when experienced traders are followed.</li>
          <li><strong>Strategy Risk:</strong> Past performance does not guarantee future results.</li>
          <li><strong>Drawdown Risk:</strong> Some traders may experience meaningful temporary losses.</li>
          <li><strong>Platform Risk:</strong> Low-transparency or poorly governed platforms may create added exposure.</li>
          <li><strong>Over-Reliance Risk:</strong> Copying others without understanding core concepts can lead to poor decisions.</li>
        </ul>
      </article>
    </div>

    <div class="copy-trading-grid">
      <article class="copy-trading-card">
        <div class="doc-page-hero-title">Best Practices</div>
        <h2>Practical ways to approach copy trading more carefully</h2>
        <ul class="copy-trading-list">
          <li>Start with a small allocation before scaling</li>
          <li>Diversify across 2-3 traders instead of relying on one</li>
          <li>Monitor maximum drawdown, not just profits</li>
          <li>Avoid traders with extreme risk or unrealistic returns</li>
          <li>Review performance monthly rather than reacting daily</li>
          <li>Keep part of your funds unallocated for flexibility</li>
        </ul>
      </article>

      <article class="copy-trading-card">
        <div class="doc-page-hero-title">Trader Selection</div>
        <h2>How to choose the right trader</h2>
        <ul class="copy-trading-list">
          <li>Prioritize consistent returns, not just high returns</li>
          <li>Look for low to moderate drawdown</li>
          <li>Prefer transparent trading history</li>
          <li>Assess whether risk management appears stable</li>
          <li>Look for at least 3-6 months of performance track record</li>
        </ul>
      </article>
    </div>

    <article class="copy-trading-disclaimer-card">
      <div class="doc-page-hero-title">Important Disclaimer</div>
      <h2>Copy trading involves significant risk</h2>
      <p>
        Copy trading involves significant risk and may not be suitable for all investors. Past performance
        of traders is not indicative of future results. You should only invest funds you can afford to lose
        and consider seeking independent financial advice if necessary.
      </p>
