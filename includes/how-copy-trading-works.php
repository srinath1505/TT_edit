    <article class="copy-trading-section-card">
      <div class="doc-page-hero-title">Step by Step</div>
      <h2>How copy trading works</h2>
      <div class="copy-trading-steps">
        <article class="copy-trading-step">
          <span>1</span>
          <h3>Create a Trading Account</h3>
          <p>Register with a platform that offers copy trading services, complete verification, and deposit funds into your account.</p>
        </article>
        <article class="copy-trading-step">
          <span>2</span>
          <h3>Choose a Trader to Follow</h3>
          <p>Platforms typically show performance metrics such as historical returns, win rate, drawdown, number of followers, and trading style.</p>
        </article>
        <article class="copy-trading-step">
          <span>3</span>
          <h3>Allocate Funds</h3>
          <p>Decide how much of your balance you want to allocate to copy trading. Trades are usually mirrored proportionally based on that allocation.</p>
        </article>
        <article class="copy-trading-step">
          <span>4</span>
          <h3>Trades Are Automatically Copied</h3>
          <p>When the selected trader opens or closes a position, the same trade is executed in your account with proportional sizing.</p>
        </article>
        <article class="copy-trading-step">
          <span>5</span>
          <h3>Profit and Loss Distribution</h3>
          <p>If the trader profits, your account benefits proportionally. If the trader loses, your account reflects the same proportional loss. Additional fees may also apply.</p>
        </article>
      </div>
    </article>

    <div class="copy-trading-grid">
