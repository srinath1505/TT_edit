<section class="leverage-margin-page">
  <div class="container">
    <article class="leverage-margin-card leverage-margin-card--feature">
      <h2 class='doc-page-hero-title'>Why this matters</h2>
      <p>
        Learning the foundations of trading is what separates a true beginner from a more confident market participant.
        Beyond the basics, features such as leverage and margin can change the size, speed, and risk profile of every trade you place.
      </p>
      <p>
        Used well, they can increase market exposure without requiring the full value of a position upfront.
        Used badly, they can amplify losses just as quickly as they amplify gains.
      </p>
    </article>

    <div class="leverage-margin-grid">
      <article class="leverage-margin-card">
        <h2 class='doc-page-hero-title'>What is leverage?</h2>
        <p>
          In trading, leverage means using a smaller amount of capital to gain exposure to a much larger market position.
          It allows your funds to go further, which can make returns larger if the trade moves in your favor.
        </p>
        <p>
          The trade-off is simple: if the market moves against you, the losses are also magnified. Leverage expands potential,
          but it also expands risk.
        </p>
      </article>

      <article class="leverage-margin-card">
        <h2 class='doc-page-hero-title'>What is margin?</h2>
        <p>
          Margin is the amount of money required to open and support a leveraged trade. It is the capital your broker uses
          as the base deposit for the position.
        </p>
        <p>
          Margin and leverage are closely connected, but they are not the same thing. Margin is the deposit. Leverage is the multiple
          of exposure that deposit helps unlock.
        </p>
      </article>
    </div>

    <article class="leverage-margin-card leverage-margin-card--section">
      <h2 class='doc-page-hero-title'>How leverage and margin work together</h2>
      <div class="leverage-margin-steps">
        <article class="leverage-margin-step">
          <span>1</span>
          <h3>You open a trade</h3>
          <p>You choose a market and decide how large a position you want to control.</p>
        </article>
        <article class="leverage-margin-step">
          <span>2</span>
          <h3>You commit margin</h3>
          <p>Instead of paying the full value of the position, you commit a smaller deposit.</p>
        </article>
        <article class="leverage-margin-step">
          <span>3</span>
          <h3>You gain larger exposure</h3>
          <p>Your broker provides the additional exposure that allows the position size to exceed your initial cash outlay.</p>
        </article>
        <article class="leverage-margin-step">
          <span>4</span>
          <h3>Your result is magnified</h3>
          <p>Profit can be larger than with an unleveraged trade, but losses can also accelerate much faster.</p>
        </article>
      </div>
    </article>

    <div class="leverage-margin-grid">
      <article class="leverage-margin-card">
        <h2 class='doc-page-hero-title'>How leveraged trading behaves in real life</h2>
        <p>
          If a leveraged position performs well, the trade cycle may feel similar to ordinary investing: when you close the trade,
          the profit is reflected in your account balance.
        </p>
        <p>
          The real difference appears when a position moves against you. Because the exposure is larger than your cash deposit,
          losses can build quickly and your broker may require additional funds to keep the trade open.
        </p>
      </article>

      <article class="leverage-margin-card leverage-margin-card--alert">
        <h2 class='doc-page-hero-title'>Margin calls and stop-outs</h2>
        <p>
          If losses reduce the usable funds in your account too far, your broker may ask you to add more capital. This is known as a margin call.
        </p>
        <p>
          If you do not add funds and the risk becomes too high, the broker may automatically reduce or close positions to limit exposure.
          This process is often referred to as a stop-out.
        </p>
      </article>
    </div>

    <article class="leverage-margin-card leverage-margin-card--section">
      <h2 class='doc-page-hero-title'>Simple example</h2>
      <div class="leverage-margin-example-grid">
        <article class="leverage-margin-panel">
          <h3>Without leverage</h3>
          <p>
            You want exposure to a $10,000 market position. Without leverage, you would need to commit the full $10,000 yourself.
          </p>
        </article>

        <article class="leverage-margin-panel">
          <h3>With leverage</h3>
          <p>
            With leveraged access, you may only need to commit a fraction of that amount as margin, while still controlling a much larger position.
          </p>
        </article>

        <article class="leverage-margin-panel">
          <h3>What changes</h3>
          <p>
            If the market moves in your favor, the return on your deposited capital can be much higher. If the market moves against you,
            the loss on that same deposit can become severe very quickly.
          </p>
        </article>
      </div>
    </article>

    <div class="leverage-margin-grid">
      <article class="leverage-margin-card">
        <h2 class='doc-page-hero-title'>Why some traders use leverage</h2>
        <ul class="leverage-margin-list">
          <li>To increase exposure without committing the full trade value upfront</li>
          <li>To pursue a higher risk-return profile</li>
          <li>To use capital more flexibly across multiple opportunities</li>
        </ul>
      </article>

      <article class="leverage-margin-card">
        <h2 class='doc-page-hero-title'>Why caution is essential</h2>
        <ul class="leverage-margin-list">
          <li>Losses are magnified as well as gains</li>
          <li>Higher volatility often means lower leverage availability</li>
          <li>Regulators may cap leverage based on asset class and market risk</li>
          <li>Emotional decision-making becomes more likely when pressure increases</li>
        </ul>
      </article>
    </div>

    <article class="leverage-margin-card leverage-margin-card--feature">
      <h2 class='doc-page-hero-title'>Trading psychology matters here</h2>
      <p>
        Leverage can easily push traders outside their comfort zone. When position sizes feel larger, people often respond to profits
        and losses in very different ways. That imbalance can distort judgment, fuel cognitive bias, and lead to poor decisions under pressure.
      </p>
      <p>
        The most important mindset shift is this: leverage should be treated as a risk tool, not a shortcut to easy returns.
      </p>
    </article>
  </div>
</section>

