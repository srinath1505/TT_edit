      <h1 class="doc-sub doc-heading-accent">
        <span data-i18n="privacyPolicyPage.heroTitle">Definitions</span>
      </h1>
      <ul class="doc-page-hero-list" style="list-style-type:none; margin-left: 0px;">

        <li><strong>Client:</strong> Any individual or entity holding a trading account with Amber Rock Trade Ltd.</li>

        <li><strong>Company:</strong> Amber Rock Trade Ltd and its affiliates, employees, or agents.</li>

        <li><strong>Order:</strong> An instruction submitted by a client to buy or sell a financial instrument.</li>

        <li><strong>Execution Venue:</strong> Authorized entities, such as exchanges, brokers, liquidity providers, or
          internal matching engines, where client orders are executed.</li>

        <li><strong>Total Consideration:</strong> The overall cost of executing an order, including the price of the
          instrument, commissions, fees, and any other costs directly related to execution.</li>

        <li><strong>Best Execution:</strong> The process of taking all reasonable steps to achieve the most favorable
          outcome for the client when executing orders, considering price, costs, speed, likelihood of execution, and
          other relevant factors.</li>

        <li><strong>Slippage:</strong> The difference between the requested execution price and the actual execution
          price. Positive slippage occurs when the execution price is better than requested; negative slippage occurs
          when it is worse.</li>

        <li><strong>Financial Instruments:</strong> All instruments offered by the Company, including Contracts for
          Difference (CFDs), Foreign Exchange (FX) contracts, and other tradable products.</li>

        <li><strong>Market Order:</strong> An order to buy or sell a financial instrument at the current market price.
        </li>

        <li><strong>Margin:</strong> Funds required to open and maintain a leveraged position.</li>

        <li><strong>Leverage:</strong> The ratio of exposure to margin.</li>

        <li><strong>Limit Order:</strong> An order to buy or sell a financial instrument at a specified price or better.
        </li>

        <li><strong>Stop Order:</strong> An order that becomes a market order once a specified stop price is reached or
          breached.</li>

        <li><strong>Good-Till-Cancelled (GTC) Order:</strong> An order that remains active until it is either executed
          or explicitly cancelled by the client.</li>

        <li><strong>Day Order:</strong> An order that expires at the end of the trading day if not executed.</li>

        <li><strong>CFD:</strong> Contract for Difference.</li>

        <li><strong>FX:</strong> Foreign Exchange.</li>

        <li><strong>OTC:</strong> Over-the-Counter.</li>

        <li><strong>Dealing Room:</strong> The Company’s internal order execution function.</li>
      </ul>
