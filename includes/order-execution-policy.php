      <h1 class="doc-sub">
        <span data-i18n="privacyPolicyPage.heroTitle">10. TYPES OF ORDERS</span>
      </h1>
      <p class="doc-page-hero-subtitle">
        Due to the risks and volatility in financial markets, clients might benefit from using different order
        types to manage their investments and risks. Please note that the following order descriptions may
        apply only to certain financial instruments and understanding the nature, duration, and associated
        risks of each order type is essential for informed trading.
      </p>
      <ul class="doc-page-hero-list" style="list-style-type:decimal">

        <li><strong>Market Order:</strong>
          <ul class="doc-page-hero-list" style="list-style-type:lower-alpha">
            <li>A market order instructs a financial institution to execute a trade as quickly as possible at the
              current market price.</li>
            <li>Financial institutions are required to execute market orders without regard for price fluctuations.
              Consequently, if the market price changes during order fulfillment, the execution may occur at a different
              price than initially requested.</li>
            <li>The execution price may be better, worse, or the same as the requested price.</li>
          </ul>
        </li>

        <li><strong>Limit Order:</strong>
          <ul class="doc-page-hero-list" style="list-style-type:lower-alpha">
            <li>A limit order specifies the maximum price to buy or minimum price to sell at which the client wants the
              trade executed.</li>
            <li>Since a limit order can be placed away from the current market price, it may not be executed
              immediately.</li>
            <li>Clients should be aware that using limit orders means relinquishing the certainty of immediate execution
              and may result in re-quotes or rejections if the market price changes during order fulfillment.</li>
          </ul>
        </li>

        <li><strong>Stop Order:</strong>
          <ul class="doc-page-hero-list" style="list-style-type:lower-alpha">
            <li>A stop order allows buying above the current market price or selling below the current market price once
              the stop price is reached or breached.</li>
            <li>The stop order remains pending until the stop price is hit, at which point it becomes a market order and
              is filled at the best available market price.</li>
            <li>It is important to note that the execution price may differ from the originally set price, which can
              result in slippage.</li>
          </ul>
        </li>

        <li><strong>Good-Till-Cancelled (GTC) Orders:</strong>
          An order that remains active until executed or explicitly cancelled.</li>

        <li><strong>Day Orders:</strong>
          An order which expires at the end of the trading day if not executed.</li>
      </ul>
      <p class="doc-page-hero-subtitle">
      </p>
