      <h1 class="doc-sub">
        <span data-i18n="privacyPolicyPage.heroTitle">8. SLIPPAGE
        </span>
      </h1>
      <p class="doc-page-hero-subtitle">
        Please be aware that slippage is a possible occurrence when trading Financial Instruments. This
        happens when the price shown during the order placement is no longer available when the order
        is executed. As a result, the order may be filled at a slightly different price, either better or worse
        than the price requested. This is referred to as slippage, with positive slippage indicating a better
        price and negative slippage indicating a worse price.
      </p>
      <p class="doc-page-hero-subtitle">
        Slippage is a normal part of trading, particularly during periods of low liquidity or high volatility,
        such as news releases, market openings, and other influential events. These situations may prevent
        execution at the exact price, leading to a different market price at the time of execution.
      </p>
      <p class="doc-page-hero-subtitle">
        While the Company seeks to minimize slippage and execute orders at the best available market
        price, execution at the exact requested price cannot be guaranteed due to market conditions such
        as low liquidity, rapid price movements, volatility, or technical delays. The Company monitors
        slippage as part of its execution quality controls and to ensure fair and consistent execution
        practices. By trading with the Company, clients acknowledge and accept the risk of slippage.
      </p>

