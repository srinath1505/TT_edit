        <li>The client’s name is identified in the World Check (i.e PEP);</li>
        <li>The client originates from a high-risk country (falls under Category 3 risk
          classification);</li>
        <li>The client does business in a high risk or sanctioned country and or in high-risk
          industry;</li>
        <li>Complex business and ownership structure;</li>
        <li>Suspicious behavior or activities;</li>
        <li>Transaction takes place in an unusual and/or significant compared to the normal
          pattern of transactions; and</li>
        <li>Transactions to/from higher-risk countries.</li>
      </ul>
