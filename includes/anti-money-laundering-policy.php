      <h1 class="doc-page-hero-title">
        <span data-i18n="privacyPolicyPage.heroTitle">2. CLIENT ACCOUNT OPENING PROCEDURES</span>
      </h1>

      <p class="doc-page-hero-subtitle">
        The necessary Due Diligence procedures, as described above, are applied to ensure
        all Know Your Client (KYC) policies are adhered to, all documents necessary for
        proceeding with KYC procedure are provided and the Company may accept the
        client and proceed to the account opening. Subsequently the client completes the
        account opening forms indicating all required information. The responsible
        employee collects all initial information of the client and the relevant department
        reviews and approves the client.<br />
        All initial client documents must be verified and approved by the Compliance
        Officer before account activation, except where limited derogation applies. The
        responsible employee collects the client’s information, which is reviewed and
        approved prior to account opening. Failure or refusal from Client to provide required
        information, the Company may suspend the account or terminate the business
        relationship, with all actions documented.<br />
        The Company also conducts periodic reviews of client information and verifies any
        changes to the client’s profile, including address, occupation, or ownership structure,
        in line with KYC policies.
      </p>
      <p class="doc-page-hero-subtitle">The AML Compliance Officer of the Company is responsible for ensuring that the
        Company complies with anti-money laundering laws and regulations. This includes
        monitoring transactions and client activity to identify suspicious behavior, reporting
        such activity to management and regulatory authorities, and maintaining accurate
        records of KYC, transactions, and compliance actions, as well as provide training to
        staff to promote awareness and a culture of compliance across the organization.</p>

      <h1 class="doc-page-hero-title">
        <span data-i18n="privacyPolicyPage.heroTitle">2.1 Client Risk Classification</span>
      </h1>
      <ul class="doc-page-hero-list" style="list-style-type:disc">
        <li><strong>Low risk customers:</strong> Individuals and entities whose identities and source of wealth can be
          efficiently determined and whose transactions and income are consistent with their economic profile shall be
          classified as low risk. It might include regulated financial institutions, governments and publicly listed
          companies.</li>
        <li><strong>Normal risk customers:</strong> Individuals and entities whose identities and source of wealth can
