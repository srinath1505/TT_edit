        unathorised access, or other suspicious behavior, in accordance with its internal
        security, anti-money laundering (AML), and counter-terrorist financing (CTF)
        procedures.
      </p>
      <p class="doc-page-hero-subtitle">All notifications related to the classification of an account as Dormant
        including but not limited to reactivation approvals, shall be duly documented
        and retained by the Company for record-keeping and audit purposes, in
        accordance with applicable regulatory and internal compliance requirements.
        Dormant accounts may present a higher risk in relation to money laundering
        and terrorist financing (ML/TF). Accordingly, the Company may conduct
        periodic internal risk assessments and reviews of Dormant accounts, even prior
        to any reactivation request.<br />
        Dormant accounts may be reactivated upon written confirmation or request by
