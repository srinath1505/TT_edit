      <h1 class="doc-page-hero-title">
        <span data-i18n="privacyPolicyPage.heroTitle">1. CLIENT IDENTIFICATION AND DUE DILIGENCE PROCEDURES
        </span>
      </h1>
      <p class="doc-page-hero-subtitle">
        The Company has adopted all requirements in relation to client identification and
        Due Diligence procedures as follows:
      </p>
      <p class="doc-page-hero-subtitle" style="font-style: italic;">
        a. Due Diligence procedures are applied in the following cases:
      </p>
      <ul class="doc-page-hero-list" style="list-style-type: disc;">
        <li>When establishing a business relationship. (opening an account, executing a
          mandate, or otherwise commencing ongoing dealings that require customer due
          diligence (CDD) and ongoing monitoring).
        </li>
        <li>When carrying out one-off transactions amounting to USD15.000 (fifteen
          thousand dollars) or more, whether the transaction is carried out in a single
          operation or in several operations which appear to be linked.</li>
        <li>When there is a suspicion of money laundering or terrorist financing, irrespective
          of the amount of the transaction.</li>
        <li>When there are doubts about the veracity or adequacy of previously obtained
          client identification data.</li>
      </ul>
      <p class="doc-page-hero-subtitle">
      </p>
      <p class="doc-page-hero-subtitle">
        Failure or refusal by a client to submit the required data and information for the
        verification of their identity, without adequate justification, constitutes elements that
        may lead to the creation of a suspicion that the client is involved in money
        laundering or terrorist financing activities. In such an event, the Company does not
        proceed with the establishment of the business relationship.
      </p>
      <p class="doc-page-hero-subtitle">
        The practice to which the Company adheres in order to comply with the
        requirements of the Act on the subject of the client identification is achieved on a
        risk-based approach where clients are classified as low, medium, or high risk based
        on geographic risk (exposure to countries with weak AML controls), product or
        service risk (complexity, cash usage, or cross-border activity), and client type risk
        (e.g., corporates, PEPs, trusts).
      </p>
      <p class="doc-page-hero-subtitle">
        High-risk clients are subject to enhanced due diligence, including additional
        verification of identity and beneficial ownership, source of funds checks, senior
        management approval, and ongoing monitoring of transactions and risk profile.
      </p>

      <p class="doc-page-hero-subtitle">
        The procedure followed by the Company is set out below:
      </p>

      <p class="doc-page-hero-subtitle" style="font-style: italic;">
        ➢ Client Due Diligence Procedure
      </p>
      <p class="doc-page-hero-subtitle">
        The Company has put in place procedures in relation to Client Identification and
        Due Diligence as listed below:
      </p>
      <ul class="doc-page-hero-list" style="list-style-type: disc;">
        <li>When establishing a business relationship
        </li>
        <li>When carrying out one-off transactions amounting to USD15.000 (fifteen thousand dollars) or more, whether
          the transaction is carried out in a single operation or in several operations which appear to be linked.</li>
        <li>When there is a suspicion of money laundering or terrorist financing, irrespective of the amount of the
          transaction.</li>
        <li>When there are doubts about the veracity or adequacy of previously obtained
          client identification data.</li>
      </ul>
      <p class="doc-page-hero-subtitle"></p>

      <p class="doc-page-hero-subtitle" style="font-style: italic;">
        ➢ Timing of identification
      </p>
      <p class="doc-page-hero-subtitle">
        Client and beneficial owner identification shall be completed before the
        establishment of a business relationship or the carrying out of a transaction.
      </p>
      <p class="doc-page-hero-subtitle">
        By way of derogation, the verification of the identity of the client and the beneficial
        owner may take place during the establishment of a business relationship if this is
        necessary so that the normal conduct of business is not interrupted and where there
        is limited risk of money laundering or terrorist financing occurring. In such situation,
        these procedures shall be completed as soon as practicable after the initial contact.
      </p>

      <p class="doc-page-hero-subtitle" style="font-style: italic;">
        ➢ Renewal of client identification
      </p>
      <p class="doc-page-hero-subtitle">
        Reviews of existing records shall take place on a regular basis, thus ensuring that the
        documents, data or information held are kept up-to-date. Client Due Diligence procedures shall be applied not
        only to all new clients but also at appropriate times
        to existing clients on a risk sensitive basis.
      </p>

      <p class="doc-page-hero-subtitle">
        b. Simplified Client Due Diligence
      </p>
      <p class="doc-page-hero-subtitle">
        Simplified procedures may apply for lower risk clients, for example a regulated
        financial institution, governmental bodies or low risk jurisdictions. More detailed
        client Due Diligence measures for lower risk clients shall apply when there is a
        suspicion of money laundering, regardless of any derogation, exemption or
        threshold, and not whenever a business relationship is established.
      </p>

      <p class="doc-page-hero-subtitle">
        It should be noted that the Company shall gather sufficient information to establish
        if the client qualifies to be classified as lower risk client.
      </p>
      <p class="doc-page-hero-subtitle" style="font-style: italic;">
        c. Enhanced Client Due Diligence
      </p>

      <p class="doc-page-hero-subtitle">
        The Company applies enhanced client Due Diligence (“EDD”) measures where its
        risk assessment identifies a higher risk of money laundering or terrorist financing,
        including in relation to high-risk clients, jurisdictions, or transactions. In particular,
        where the client has not been physically present for identification purposes, the
        Company shall apply specific and adequate measures to mitigate the identified risks
        by implementing one or more of the following measures:
      </p>
      <ul class="doc-page-hero-list" style="list-style-type: disc;">
        <li>Ensuring that the client’s identity is established by additional documents, data or
          information. The Company applies compensating measures such as certified
          documents, video verification, or third-party verification.</li>
        <li>Supplementary measures to verify or certify the documents supplied or requiring
          confirmatory certification by a credit or financial institution.</li>
        <li>Ensuring that the first payment of the operations is carried out through an account
          opened in the client’s name with a reputable credit institution.
        </li>
        <li>High-risk relationships are subject to enhanced ongoing monitoring.</li>
      </ul>
      <p class="doc-page-hero-subtitle"></p>
      <p class="doc-page-hero-subtitle" style="font-style: italic;">
        d. Politically Exposed Persons
      </p>
      <ul class="doc-page-hero-list" style="list-style-type: disc;">
        <li>Take appropriate measures for the establishment of the origin of the client’s
          assets and the source of funds that are related with the establishment of the
          business relationship or transaction.</li>
        <li>Conduct enhanced and continuous monitoring of the business relationship.
        </li>
        <li>Conduct further Due Diligence by requesting a bank statement and additional
          identification.
        </li>
      </ul>
      <p class="doc-page-hero-subtitle"></p>
      <p class="doc-page-hero-subtitle">
        Requests references or confirmation of duties in public office. Politically exposed
        persons (PEP): Means the natural persons whether domestic or foreign, in
        accordance with international best practices and regulatory expectations. The
        classification of a PEP shall not be affected by the country of origin or the
        jurisdiction in which the individual holds or has held a prominent public function
        and who are or have been entrusted with prominent public functions and their
        immediate family members or persons known to be close associates of such persons.
      </p>
      <p class="doc-page-hero-subtitle">
        The Company defines a PEP as an individual who is or in the last 12 months has
        been entrusted with a prominent function and as such could potentially abuse such
        position or function for the purposes of laundering or other predicate offences, such
        as corruption or bribery. Due to the high risk associated with PEPs, additional EDD
        measures are put into place when entering into a business relationship with a PEP.
      </p>
      <p class="doc-page-hero-subtitle">
        The Company, in addition to screening via WiseBOS, utilises existing commercial
        resources and other databases for the identification of PEPs and always ensures that
        initial KYC and due diligence include reviewing individual names against these
        resources and databases to identify PEPs immediately.
      </p>
      <p class="doc-page-hero-subtitle">
        The Company maintains PEP lists that are reviewed and updated periodically to
        ensure timely identification of any changes in an individual’s status, role, or risk
        profile. Such reviews shall occur at regular intervals and upon the occurrence of any
        significant event that may impact PEP designation and record retention is for a
        period of 7 years after business relationship ends.
      </p>
      <p class="doc-page-hero-subtitle">
        Where the Company decides to form a business relationship with PEP, always
        ensure that:
      </p>
      <ul class="doc-page-hero-list" style="list-style-type: disc;">
        <li>Risk Committee (RC) approval for establishing the business relationship is
          obtained and recorded;</li>
        <li>We take reasonable measures to establish the source of wealth and source of funds;
        </li>
        <li>We conduct Due Diligence on prospective client;
        </li>
        <li>We conduct enhanced and continues monitoring of the business relationship;
        </li>
        <li>Due diligence is performed on PEPs who are not our clients but are connected to
          one of our corporate clients by statutes or via notion of control;
        </li>
        <li>DD is performed when dealing with those who are PEPs, as well as family
          members or close associates of those PEPs.
        </li>
      </ul>
      <p class="doc-page-hero-subtitle">
      </p>
      <p class="doc-page-hero-subtitle">
        The Risk Committee (“RC”) is responsible for reviewing and approving the
        establishment or continuation of business relationships involving high-risk clients
        or Politically Exposed Persons (“PEPs”). The RC’s decision shall be based on
        documented risk assessment findings, including the client’s risk profile, source of
        funds and wealth, jurisdictional exposure, and the adequacy of proposed risk
        mitigation measures. All approvals or rejections must be documented in writing,
        securely stored, and made available for audit or regulatory review. The RC may
        approve a relationship only where the identified risks are deemed manageable and
        adequately mitigated; otherwise, the relationship shall be rejected or terminated.
      </p>
      <p class="doc-page-hero-subtitle">
        A person who is a PEP shall continue to be treated as such for at least 12 months
        after the date on which that person ceased to be entrusted with that public function; or for such longer period
        as Company considers appropriate to address risks of
        money laundering or terrorist financing in relation to that person. These provisions
        do not apply to family members who should be treated as ordinary customers, unless
        other risks are apparent, from the point that the PEP leaves office.
      </p>

