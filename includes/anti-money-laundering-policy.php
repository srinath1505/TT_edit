      <h1 class="doc-page-hero-title">
        <span data-i18n="privacyPolicyPage.heroTitle">Purpose</span>
      </h1>
      <p class="doc-page-hero-subtitle" data-i18n="privacyPolicyPage.heroSubtitle">
        The purpose of this policy is to provide guidance on the Anti-Money Laundering
        and Know Your Client Policy which is followed by Amber Rock Trade Ltd
        hereinafter called ‘’the Company’’ to achieve full compliance with the relevant
        Anti-Money Laundering legislation.
      </p>
      <h1 class="doc-page-hero-title">
        <span data-i18n="privacyPolicyPage.heroTitle">Legal Framework</span>
      </h1>
      <p class="doc-page-hero-subtitle">
        Investment Firms are required to comply with the provisions of the Anti-Money
        Laundering and Anti-Terrorism Financing Act 2002 (hereinafter called the “Act”
        for the purposes of this policy). The main purpose of the Act is to ensure that the
        Company complies with relevant anti-money laundering and combating funding
        terrorism laws and regulations, assist law enforcement in combating illegal money
        laundering, and minimizes the risk of the Company’s resources being used for
        improper purposes.
      </p>
      <p class="doc-page-hero-subtitle">
        In accordance with the Act, Investment Firms are obliged to set out policies and
        procedures for preventing money laundering activities. Those procedures, which
        are implemented by the Company, as these are requested by the Act, are the
        following:
      </p>
      <ul class="doc-page-hero-list" style="list-style-type: lower-alpha;">
        <li>Record keeping procedures in relation to clients’ identity and their
          transactions. All personal data collected for AML purposes shall comply with GDPR
          and applicable local data protection laws and be processed only as necessary for
          compliance.</li>
        <li>Compliance officers at management level in each branch and subsidiary who
          will be in charge of the application of the internal programs and procedures,
          including proper maintenance of records and reporting of suspicious transactions.</li>
        <li>Appropriate procedures of internal control, risk management, with the
          purpose of preventing money laundering activities.
        </li>
        <li>The detailed examination of every transaction that due to its nature is
          considered vulnerable to money laundering, and especially for complicated or
          unusually large transactions and transactions that take place without an obvious
          financial or legal purpose.</li>
        <li>Measures for making employees aware of the procedures mentioned above
          set forth to prevent money laundering and of the legislation relating to money
          laundering.</li>
        <li>Provision of regular annually training to their employees in the recognition
          and handling of transactions suspected to be associated with money laundering.
        </li>
        <li>Audit functions to evaluate such policies, procedures and controls to test
          compliance with the measures taken by the reporting institution to comply with the
          provisions of this Act and the effectiveness of such measures.
        </li>
      </ul>
      <p class="doc-page-hero-title"></p>

